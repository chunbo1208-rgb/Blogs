// generate-posts-data.js
// Scans all post HTML files, reads <post-meta> attributes, writes posts-data.js.
// Run: node generate-posts-data.js
// Cloudflare Pages build command: node generate-posts-data.js

const fs   = require('fs');
const path = require('path');

function scanDir(dir) {
  const results = [];
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      results.push(...scanDir(full));
    } else if (entry.isFile() && entry.name.endsWith('.html') && !entry.name.endsWith('.index.html')) {
      results.push(full);
    }
  }
  return results;
}

function attr(tag, name) {
  const m = tag.match(new RegExp(`${name}="([^"]*)"`));
  return m ? m[1] : '';
}

function listAttr(tag, name) {
  return attr(tag, name)
    .split(',')
    .map(item => item.trim())
    .filter(Boolean);
}

function kindFromSubject(subject) {
  if (subject === 'poem') return 'poem';
  if (subject === 'ideas') return 'essay';
  return 'note';
}

function parsePost(filePath) {
  const html  = fs.readFileSync(filePath, 'utf8');
  const match = html.match(/<post-meta([\s\S]*?)>/);
  if (!match) return null;
  const tag = match[1];
  const id  = attr(tag, 'id');
  if (!id) return null;
  return {
    id,
    path:           path.relative(__dirname, filePath).replace(/\\/g, '/'),
    title:          attr(tag, 'title'),
    author:         attr(tag, 'author'),
    date:           attr(tag, 'date'),
    subject:        attr(tag, 'subject'),
    custom_subject: attr(tag, 'custom_subject'),
    language:       attr(tag, 'language'),
    template:       attr(tag, 'template'),
    kind:           attr(tag, 'kind') || kindFromSubject(attr(tag, 'subject')),
    description:    attr(tag, 'description'),
    tags:           listAttr(tag, 'tags'),
  };
}

const postsDir = path.join(__dirname, 'posts');
const posts = scanDir(postsDir)
  .map(parsePost)
  .filter(Boolean)
  .sort((a, b) => b.date.localeCompare(a.date));

const output =
  `// posts-data.js — auto-generated by generate-posts-data.js\n` +
  `// Do not edit manually. Run: node generate-posts-data.js\n\n` +
  `window.POSTS = ${JSON.stringify(posts, null, 2)};\n`;

fs.writeFileSync(path.join(__dirname, 'posts-data.js'), output, 'utf8');
console.log(`Generated posts-data.js — ${posts.length} posts.`);
